public class DoublevaluemethodApp
{
	public static void main(String x[])
	{
		Integer i=1566;
		Float d=2625.62f;
		Long l=2695l;
		
		double q=i.doubleValue();
		double w=d.doubleValue();
		double e=l.doubleValue();
		System.out.println("it is a float data "+q );
		System.out.println("it is a integer data "+w );
		System.out.println("it is a double data "+e );
		
	}
}